import { toast } from "react-toastify";
export const shownotifySuccess = (textToast) => {
  return toast.success(textToast, {
    position: "top-right",
    autoClose: 1000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "colored",
  });
};

